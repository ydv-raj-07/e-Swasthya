DROP TABLE IF EXISTS Punjab_Health_Department;
CREATE TABLE Punjab_Health_Department (
user_id INT PRIMARY KEY AUTO_INCREMENT,
name VARCHAR (100) NOT NULL,
email VARCHAR(100) NOT NULL UNIQUE,
password VARCHAR(255) NOT NULL,
role
ENUM('ADMIN' , 'OFFICER' , 'VERIFIER' ) NOT NULL DEFAULT 'OFFICER',
status
ENUM('PENDING' , 'ACTIVE' , 'INACTIVE') DEFAULT 'PENDING',
created_at TIMESTAMP
DEFAULT CURRENT_TIMESTAMP,
last_login TIMESTAMP NULL);

SELECT * FROM Punjab_Health_Department;
